package oparetor;

public class IncremetDecrementDemo {
	
	public static void main (String []args ) {
		
		int x = 25;
		int y;
		
	
		
		y=--x;
		System.out.println("y="+y);//predecrement..result be 24.		
		y=x--;
		System.out.println("Y="+y);//postdecement..result be 24.
		
		y=++x;
		System.out.println("y="+y);//preincrement.result be 24-1=23+1+=24.
																				
		
		y=x++;
		System.out.println("y="+y);//postdecreent..result be 24.
		
	
		
		
		
		
		
	}

}
