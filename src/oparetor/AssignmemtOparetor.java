package oparetor;

public class AssignmemtOparetor {

	public static void main (String[]args) {
		
		int x=3;
		int y=2;
		
		x+=y;
		System.out.println ("x="+x);//x=x+y
		
		x-=y;
		System.out.println ("x="+x);//X=x-y
		
		x*=y;
		System.out.println("x="+x);//x=x*y
		
		x/=y;
		System.out.println("x="+x);//x=x/y
		
		x%=y;
		System .out.println("x"+x);//x=x%y
		
			
		
		
	}

}
